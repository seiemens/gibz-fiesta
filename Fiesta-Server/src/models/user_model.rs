use mongodb::bson::oid::ObjectId;
use serde::{Deserialize, Serialize};

#[derive(Serialize, Deserialize, Clone, Debug, PartialEq)]
pub struct User {
    pub _id: Option<ObjectId>,
    pub name: Option<String>,
    pub username: String, // mandatory
    pub password: String,
    pub email: Option<String>,
    pub role: Option<i32>, // 0..2 -> 0:Admin, 1:Teacher, 2:Guest
    pub field: Option<String>,
    pub completed_skills: Option<Vec<String>>, //unaccessible if role > 1
    pub marked_skills: Option<Vec<ObjectId>>,  //unaccessible if role > 1
    pub auth_token: Option<String>,            //generated by backend
    pub active: Option<bool>,
}
